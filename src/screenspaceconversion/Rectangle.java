public class Rectangle {
    
    public double leftX;
    public double rightX;
    public double topY;
    public double bottomY;
    
    public Rectangle(double leftX, double rightX, double topY, double bottomY) {
        this.leftX = leftX;
        this.rightX = rightX;
        this.topY = topY;
        this.bottomY = bottomY;
    }
}